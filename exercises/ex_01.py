# We define a variable x containing a string
x = "The value of (a + b) * c is"

# We can make multiple assignments in a single line
a, b, c = 1, 2, 3

# Let's define the operation we want to perform
d = (a + b) * c

# Let's define a boolean variable
printing = True

# if printing is true, then use print()
if printing:
    print(x, d)

# Expected output: The value of (a + b) * c is 9